"""
SALVO I:
=================
דרגת קושי: 4
-----------------
המשחק "קרב ימי" (Salvo I) הוא משחק טקסטואלי שבו השחקן מנסה להטביע ספינות שממוקמות על ידי המחשב על לוח משחק בגודל 10 על 10. השחקן מזין קואורדינטות ירי, והמחשב מדווח על התוצאה (פגיעה, החטאה או ירי חוזר לאותה נקודה). המשחק מסתיים כאשר כל ספינות האויב הוטבעו.

חוקי המשחק:
1. לוח המשחק – ריבוע בגודל 10 על 10.
2. המחשב ממקם 5 ספינות.
3. השחקן מזין קואורדינטות ירי בפורמט "X,Y" (לדוגמה, "2,5").
4. המחשב מדווח על תוצאת הירי: "HIT", "MISS" או "ALREADY HIT".
5. המשחק ממשיך עד שהשחקן מטביע את כל הספינות.
-----------------
אלגוריתם:
1. אתחול לוח המשחק בגודל 10x10, ממולא באפסים (ים ריק).
2. מיקום 5 ספינות על לוח המשחק (קואורדינטות מוגדרות באופן קשיח בקוד).
3. הגדרת מונה פגיעות ל-0.
4. תחילת לולאה "כל עוד מספר הפגיעות קטן מ-5":
    4.1 בקשת קלט מהשחקן עבור קואורדינטות ירי בפורמט "X,Y".
    4.2 בדיקה האם ירי זה בוצע בעבר (אם בקואורדינטה אין 0, הירי בוצע).
       4.2.1 אם הירי בוצע, הודעה לשחקן על כך וחזרה לבקשת קואורדינטות.
    4.3 אם הירי פגע בספינה:
       4.3.1 הגדלת מונה הפגיעות ב-1.
       4.3.2 הודעה לשחקן על הפגיעה ("HIT").
       4.3.3 שינוי הערך בקואורדינטה ל-1 (פגיעה).
    4.4 אם הירי החטיא:
       4.4.1 הודעה לשחקן על ההחטאה ("MISS").
       4.4.2 שינוי הערך בקואורדינטה ל-2 (החטאה).
5. כאשר מספר הפגיעות מגיע ל-5, הצגת הודעת ניצחון.
6. סיום המשחק.
-----------------
תרשים זרימה:
```mermaid
flowchart TD
    Start["התחלה"] --> InitializeBoard["אתחול לוח המשחק: 10x10 (ממולא באפסים)"]
    InitializeBoard --> PlaceShips["מיקום 5 ספינות על הלוח (קואורדינטות מוגדרות קשיח)"]
    PlaceShips --> InitializeHits["אתחול מונה פגיעות: <code>hits = 0</code>"]
    InitializeHits --> LoopStart{"תחילת לולאה: כל עוד <code>hits < 5</code>"}
    LoopStart -- כן --> InputCoordinates["קלט קואורדינטות ירי: <code>X,Y</code>"]
    InputCoordinates --> CheckPreviousHit{"בדיקה: האם ירי בקואורדינטות אלו כבר בוצע?"}
    CheckPreviousHit -- כן --> OutputAlreadyHit["פלט הודעה: <b>ALREADY HIT</b>"]
    OutputAlreadyHit --> InputCoordinates
    CheckPreviousHit -- לא --> CheckHit{"בדיקה: פגע בספינה?"}
    CheckHit -- כן --> IncreaseHits["<code>hits = hits + 1</code>"]
    IncreaseHits --> OutputHit["פלט הודעה: <b>HIT</b>"]
    OutputHit --> MarkHit["סימון פגיעה על הלוח"]
    MarkHit --> LoopStart
    CheckHit -- לא --> OutputMiss["פלט הודעה: <b>MISS</b>"]
    OutputMiss --> MarkMiss["סימון החטאה על הלוח"]
    MarkMiss --> LoopStart
    LoopStart -- לא --> OutputWin["פלט הודעה: <b>YOU SANK ALL MY SHIPS!</b>"]
    OutputWin --> End["סיום"]
```

מקרא:
    Start - תחילת התוכנית.
    InitializeBoard - יצירה ואתחול של לוח המשחק 10x10, ממולא באפסים.
    PlaceShips - מיקום הספינות על לוח המשחק. קואורדינטות הספינות מוגדרות באופן קשיח בקוד.
    InitializeHits - אתחול מונה הפגיעות, שמתחיל מאפס.
    LoopStart - תחילת הלולאה, שנמשכת כל עוד מספר הפגיעות קטן מ-5.
    InputCoordinates - בקשת קואורדינטות ירי מהמשתמש בפורמט "X,Y".
    CheckPreviousHit - בדיקה האם כבר בוצע ירי בקואורדינטות אלו.
    OutputAlreadyHit - הצגת ההודעה "ALREADY HIT", אם הירי בקואורדינטות אלו בוצע בעבר.
    CheckHit - בדיקה האם הירי פגע בספינה.
    IncreaseHits - הגדלת מונה הפגיעות ב-1.
    OutputHit - הצגת ההודעה "HIT" בעת פגיעה בספינה.
    MarkHit - סימון פגיעה על לוח המשחק (בדרך כלל עם המספר 1).
    OutputMiss - הצגת ההודעה "MISS" בעת החטאה.
    MarkMiss - סימון החטאה על לוח המשחק (בדרך כלל עם המספר 2).
     OutputWin - הצגת הודעה על הניצחון, כאשר כל הספינות הוטבעו.
    End - סיום התוכנית.
"""
import sys



# אתחול לוח המשחק 10x10 עם אפסים
board = [[0 for _ in range(10)] for _ in range(10)]

# קואורדינטות הספינות (מוגדרות באופן קשיח)
ships = [(2, 2), (2, 4), (2, 6), (4, 2), (4, 8)]

# מיקום הספינות על הלוח, סימונן עם הערך 3
for ship_x, ship_y in ships:
    board[ship_x][ship_y] = 3


# מונה פגיעות
hits = 0
# לולאת המשחק הראשית
while hits < 5:
    try:
        # בקשת קואורדינטות ירי
        coordinates_input = input("הזן קואורדינטות ירי (x,y): ")
        x, y = map(int, coordinates_input.split(','))
        # בדיקה על תקינות קלט הקואורדינטות
        if not (0 <= x < 10 and 0 <= y < 10):
            print("קואורדינטות לא תקינות. אנא הזן ערכים בין 0 ל-9.")
            continue

        # בדיקה האם ירי בקואורדינטות אלו כבר בוצע
        if board[x][y] == 1 or board[x][y] == 2:
            print("ALREADY HIT")
            continue
        # בדיקה האם פגענו בספינה
        if board[x][y] == 3:
            hits += 1 # הגדלת מונה הפגיעות
            print("HIT")
            board[x][y] = 1 # סימון פגיעה על הלוח (ערך 1)
        else:
            print("MISS")
            board[x][y] = 2  # סימון החטאה על הלוח (ערך 2)
    except ValueError:
        print("פורמט קלט שגוי. הזן קואורדינטות בפורמט x,y")
    except KeyboardInterrupt:
       print("\nהמשחק הופסק על ידי המשתמש.")
       sys.exit()

# הצגת הודעה על ניצחון
print("YOU SANK ALL MY SHIPS!")
