import random

def roll_dice():
    """מטיל שתי קוביות משחק ומחזיר את ערכיהן."""
    dice1 = random.randint(1, 6)
    dice2 = random.randint(1, 6)
    return dice1, dice2

def calculate_sum(dice1, dice2):
  """מחשב את סכום הערכים של שתי קוביות."""
  return dice1 + dice2

def play_craps():
    """מפעיל את משחק הקרפס."""
    # הטלה ראשונה של קוביות
    dice1, dice2 = roll_dice()
    sum_of_dice = calculate_sum(dice1,dice2)
    print(f"התקבלו: {dice1} + {dice2} = {sum_of_dice}")

    # בדיקת ניצחון או הפסד לאחר ההטלה הראשונה
    if sum_of_dice == 7 or sum_of_dice == 11:
        print("YOU WIN!")
        return
    elif sum_of_dice == 2 or sum_of_dice == 3 or sum_of_dice == 12:
        print("YOU LOSE!")
        return

    # קביעת מספר היעד
    point = sum_of_dice
    print(f"מספר היעד שלך: {point}")

    # ממשיכים להטיל קוביות עד שננצח או נפסיד
    while True:
      dice1, dice2 = roll_dice()
      sum_of_dice = calculate_sum(dice1,dice2)
      print(f"התקבלו: {dice1} + {dice2} = {sum_of_dice}")
      if sum_of_dice == point:
          print("YOU WIN!")
          return
      elif sum_of_dice == 7:
          print("YOU LOSE!")
          return

if __name__ == "__main__":
    play_craps()
