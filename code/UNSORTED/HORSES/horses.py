import random

# מייצרים מספר אקראי בין 1 ל-7 (מספר הסוס המנצח)
winningHorse = random.randint(1, 7)

# מבקשים מהמשתמש להזין מספר (מספר הסוס שבו הוא משחק)
try:
    userHorse = int(input("Выберите номер лошади от 1 до 7: "))
    if userHorse < 1 or userHorse > 7:
      print("Пожалуйста, выберите номер лошади от 1 до 7.")
      exit()
except ValueError:
    print("Пожалуйста, введите целое число от 1 до 7.")
    exit()


# בודקים אם המשתמש ניצח
if userHorse == winningHorse:
    print("УРА! ВАША ЛОШАДЬ ВЫИГРАЛА!") # הדפסת הודעת ניצחון
else:
    print("К СОЖЕЛЕНИЮ, ВЫ ПРОИГРАЛИ!") # הדפסת הודעת הפסד

