"""
CUBE
=================

רמת קושי: 5
-----------------

המשחק "קובייה" הוא משחק פאזל שבו השחקן צריך להרכיב קובייה על ידי הזזת הפאות שלה. הקובייה מיוצגת כמטריצה בגודל 3x3, כאשר כל תא מייצג פאה של הקובייה. השחקן יכול להזיז את פאות הקובייה מעלה, מטה, שמאלה וימינה. מטרת המשחק היא להרכיב את הקובייה על ידי סידור הפאות בסדר הנכון.
כללי המשחק:
1. הקובייה מיוצגת כמטריצה בגודל 3x3.
2. השחקן יכול להזיז את פאות הקובייה על ידי הזנת פקודות: U (מעלה), D (מטה), L (שמאלה), R (ימינה).
3. מטרת המשחק היא להרכיב את הקובייה על ידי סידור הפאות בסדר הנכון.
4. עמדת ההתחלה של הקובייה נוצרת באופן אקראי.
5. המשחק מסתיים כאשר הקובייה הורכבה, כלומר כאשר כל הפאות מסודרות בסדר הנכון.
-----------------
אלגוריתם:
1. אתחול הקובייה עם ערכים אקראיים מ-1 עד 9 בצורת מטריצה 3x3.
2. הצגת הקובייה על המסך.
3. התחלת לולאת משחק:
    3.1. בקשת קלט מהשחקן לפקודת הזזת פאת הקובייה (U, D, L, R).
    3.2. ביצוע הזזת הפאה בהתאם לפקודה:
       - אם הפקודה "U", אז להזיז את כל השורות למעלה.
       - אם הפקודה "D", אז להזיז את כל השורות למטה.
       - אם הפקודה "L", אז להזיז את כל העמודות שמאלה.
       - אם הפקודה "R", אז להזיז את כל העמודות ימינה.
    3.3. הצגת הקובייה על המסך.
    3.4. בדיקה האם הקובייה הורכבה.
    3.5. אם הקובייה הורכבה, הצגת הודעת ניצחון וסיום המשחק.
    3.6. אם הקובייה לא הורכבה, חזרה לשלב 3.1.
-----------------

"""
import random

def generate_cube():
    """מייצרת קובייה אקראית בגודל 3x3."""
    numbers = list(range(1, 10))
    random.shuffle(numbers)
    cube = [numbers[i:i+3] for i in range(0, 9, 3)]
    return cube

def display_cube(cube):
    """מציגה את הקובייה על המסך."""
    for row in cube:
        print('  '.join(map(str, row)))

def move_cube(cube, move):
    """מזיזה את פאות הקובייה."""
    if move == 'U':
        # מזיזים את כל השורות למעלה
        temp = cube[0]
        for i in range(2):
            cube[i] = cube[i+1]
        cube[2] = temp
    elif move == 'D':
        # מזיזים את כל השורות למטה
        temp = cube[2]
        for i in range(2, 0, -1):
            cube[i] = cube[i-1]
        cube[0] = temp
    elif move == 'L':
        # מזיזים את כל העמודות שמאלה
        temp = [row[0] for row in cube]
        for i in range(3):
            for j in range(2):
                cube[i][j] = cube[i][j+1]
            cube[i][2] = temp[i]
    elif move == 'R':
         # מזיזים את כל העמודות ימינה
        temp = [row[2] for row in cube]
        for i in range(3):
            for j in range(2, 0, -1):
                cube[i][j] = cube[i][j-1]
            cube[i][0] = temp[i]
    return cube

def is_solved(cube):
  """בודקת האם הקובייה הורכבה."""
  # בדיקה שכל המספרים מסודרים בסדר עולה מ-1 עד 9
  expected = [1, 2, 3, 4, 5, 6, 7, 8, 9]
  actual = []
  for row in cube:
    actual.extend(row)
  return actual == expected

# אתחול הקובייה
cube = generate_cube()
# הצגת הקובייה על המסך
display_cube(cube)

# לולאת המשחק הראשית
while True:
    # מבקשים קלט הזזה מהמשתמש
    move = input("הזן הזזה (U, D, L, R): ").upper()
    # בודקים תקינות ההזזה שהוזנה
    if move not in ['U', 'D', 'L', 'R']:
        print("הזזה שגויה. נסה שוב.")
        continue
    # מזיזים את פאות הקובייה
    cube = move_cube(cube, move)
    # הצגת הקובייה על המסך
    display_cube(cube)
    # בודקים האם הקובייה הורכבה
    if is_solved(cube):
        print("מזל טוב! הרכבת את הקובייה!")
        break # מסיימים את הלולאה אם הקובייה הורכבה

