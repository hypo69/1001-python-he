"""
SPLAT:
=================
רמת קושי: 3
-----------------
המשחק "SPLAT" מדמה מצב שבו השחקן נדרש לנחש את מיקום הנפילה של "טיפה" על ציר מספרים בין 0 ל-100. השחקן מזין את השערותיו, והמשחק מודיע עד כמה ההשערה קרובה למטרה, באמצעות המונחים "Far Out" (רחוק), "Close" (קרוב), או "SPLAT!!!" במקרה של פגיעה מדויקת.

חוקי המשחק:
1. המחשב מייצר מספר שלם אקראי בין 0 ל-100, המשמש כמטרה.
2. השחקן מזין את השערתו לגבי מיקום הטיפה על ציר המספרים.
3. אם ההשערה נמצאת במרחק של 10 יחידות או יותר מהמטרה, מוצגת ההודעה "Far Out".
4. אם ההשערה נמצאת במרחק של פחות מ-10 יחידות מהמטרה, מוצגת ההודעה "Close".
5. אם ההשערה חופפת בדיוק למטרה, מוצגת ההודעה "SPLAT!!!" והמשחק מסתיים.
6. המשחק נמשך עד שהשחקן מנחש את מיקום הטיפה המדויק.
-----------------
אלגוריתם:
1. לייצר מספר שלם אקראי בטווח שבין 0 ל-100 ולהקצות אותו למשתנה targetNumber.
2. להתחיל לולאה: כל עוד המטרה לא נוחשה:
    2.1 לבקש מהשחקן להזין מספר (userGuess).
    2.2 לחשב את הערך המוחלט של ההפרש בין userGuess ל-targetNumber ולהקצות אותו למשתנה distance.
    2.3 אם distance שווה ל-0, להציג את ההודעה "SPLAT!!!" ולסיים את המשחק.
    2.4 אם distance גדול או שווה ל-10, להציג את ההודעה "Far Out".
    2.5 אם distance קטן מ-10, להציג את ההודעה "Close".
3. לחזור לשלב 2.
-----------------
תרשים זרימה:
```mermaid
flowchart TD
    Start["התחלה"] --> InitializeTarget["<p align='left'>אתחול: 
    <code><b>
    targetNumber = random(0, 100)
    </b></code></p>"]
    InitializeTarget --> LoopStart{"תחילת לולאה: כל עוד המטרה לא נוחשה"}
    LoopStart --> InputGuess["קלט מספר מהמשתמש: <code><b>userGuess</b></code>"]
    InputGuess --> CalculateDistance["<code><b>distance = abs(userGuess - targetNumber)</b></code>"]
    CalculateDistance --> CheckSplat{"בדיקה: <code><b>distance == 0?</b></code>"}
    CheckSplat -- כן --> OutputSplat["פלט הודעה: <b>SPLAT!!!</b>"]
    OutputSplat --> End["סיום"]
    CheckSplat -- לא --> CheckFarOut{"בדיקה: <code><b>distance &gt;= 10?</b></code>"}
    CheckFarOut -- כן --> OutputFarOut["פלט הודעה: <b>Far Out</b>"]
    OutputFarOut --> LoopStart
    CheckFarOut -- לא --> OutputClose["פלט הודעה: <b>Close</b>"]
    OutputClose --> LoopStart
    
```
מקרא:
    התחלה - תחילת התוכנית.
    אתחול - אתחול: מיוצר מספר אקראי בין 0 ל-100 ומוקצה למשתנה targetNumber.
    תחילת לולאה - תחילת הלולאה, שנמשכת כל עוד השחקן לא מנחש את המספר (distance לא הופך ל-0).
    קלט מספר מהמשתמש - בקשת קלט מהמשתמש עבור מספר (מיקום הנפילה המשוער) ושמירתו במשתנה userGuess.
    חישוב מרחק - חישוב הערך המוחלט של ההפרש בין המספר שהוזן userGuess לבין המספר המטרה targetNumber, התוצאה נשמרת במשתנה distance.
    בדיקה - בדיקה האם distance שווה ל-0.
    פלט הודעה - פלט ההודעה "SPLAT!!!" במקרה ש-distance שווה ל-0.
    סיום - סיום התוכנית.
    בדיקה - בדיקה האם distance גדול או שווה ל-10.
    פלט הודעה - פלט ההודעה "Far Out", אם distance גדול או שווה ל-10.
    פלט הודעה - פלט ההודעה "Close", אם distance קטן מ-10 ואינו שווה ל-0.
"""
import random

# מייצר מספר אקראי בין 0 ל-100
targetNumber = random.randint(0, 100)

# לולאה אינסופית, כל עוד השחקן לא ניחש את המספר
while True:
    # מבקש קלט מספר מהמשתמש
    try:
        userGuess = int(input("הזן את השערתך (בין 0 ל-100): "))
    except ValueError:
        print("אנא הזן מספר שלם.")
        continue
    
    # מחשב את המרחק בין המספר שהוזן למספר המטרה
    distance = abs(userGuess - targetNumber)

    # בודק האם המספר נוחש
    if distance == 0:
        print("SPLAT!!!")
        break  # מסיים את הלולאה אם המספר נוחש
    elif distance >= 10:
        print("Far Out")  # מודיע שהמספר רחוק מהמטרה
    else:
        print("Close")  # מודיע שהמספר קרוב למטרה

"""
הסבר על הקוד:
1.  **ייבוא מודול `random`**:
   -   `import random`: מייבא את המודול `random`, המשמש לייצור מספר אקראי.
2.  **ייצור מספר אקראי**:
   -   `targetNumber = random.randint(0, 100)`: מייצר מספר שלם אקראי בטווח שבין 0 ל-100 (כולל הקצוות) ושומר אותו במשתנה `targetNumber`. מספר זה מייצג את "מקום נפילת הטיפה".
3.  **לולאת המשחק הראשית `while True:`**:
   -   לולאה זו תתבצע כל עוד השחקן לא ניחש את המספר הנכון.
4.  **קלט נתונים**:
    -   `try...except ValueError`: בלוק `try-except` מטפל בשגיאות קלט אפשריות. אם המשתמש יזין ערך שאינו מספר שלם, תוצג הודעת שגיאה מתאימה.
    -   `userGuess = int(input("הזן את השערתך (בין 0 ל-100): "))`: מבקש מהמשתמש להזין מספר וממיר אותו למספר שלם, שומר את התוצאה ב-`userGuess`. זוהי השערת השחקן לגבי מקום נפילת הטיפה.
5.  **חישוב המרחק**:
    -   `distance = abs(userGuess - targetNumber)`: מחשב את הערך המוחלט של ההפרש בין המספר שהוזן `userGuess` למספר המטרה `targetNumber`. זהו המרחק בין השערת השחקן למקום נפילת הטיפה בפועל.
6.  **בדיקה האם הייתה פגיעה**:
   -   `if distance == 0:`: בודק האם המרחק שווה לאפס. אם כן, פירושו שהשחקן ניחש את מקום הנפילה המדויק.
    - `print("SPLAT!!!")`: מציג את ההודעה "SPLAT!!!" כאשר השחקן ניחש נכון.
    - `break`: מסיים את הלולאה כאשר השחקן ניחש נכון.
7. **בדיקת המרחק**
    - `elif distance >= 10:`: בודק אם המרחק גדול או שווה ל-10.
    - `print("Far Out")`: מציג את ההודעה "Far Out", כאשר השחקן רחוק מהמטרה.
    - `else:`: אם המרחק קטן מ-10, אזי:
    - `print("Close")`: מציג את ההודעה "Close", כאשר השחקן קרוב למטרה.
"""