# פונקציה להצגת לוח המשחק
def print_board(board):
    """
    מציגה את המצב הנוכחי של לוח המשחק.

    Args:
        board: רשימה המייצגת את לוח המשחק.
    """
    print("\n   " + board[1] + " | " + board[2] + " | " + board[3])
    print("  ---+---+---")
    print("   " + board[4] + " | " + board[5] + " | " + board[6])
    print("  ---+---+---")
    print("   " + board[7] + " | " + board[8] + " | " + board[9] + "\n")


# פונקציה לבדיקת ניצחון
def check_win(board, player):
    """
    בודקת האם קיימת קומבינציית ניצחון עבור השחקן הנתון.

    Args:
        board: רשימה המייצגת את לוח המשחק.
        player: סימן השחקן ('X' או 'O').

    Returns:
        True אם השחקן ניצח, אחרת False.
    """
    # כל הקומבינציות האפשריות לניצחון
    winning_combinations = [
        [1, 2, 3], [4, 5, 6], [7, 8, 9],  # אופקיות
        [1, 4, 7], [2, 5, 8], [3, 6, 9],  # אנכיות
        [1, 5, 9], [3, 5, 7]  # אלכסוניות
    ]
    # עוברים על כל קומבינציה ובודקים האם היא תואמת לסימני השחקן
    for combination in winning_combinations:
        if (board[combination[0]] == board[combination[1]] ==
                board[combination[2]] == player):
            return True
    return False


# פונקציה לבדיקת תיקו
def check_draw(board):
    """
    בודקת האם המשחק הנוכחי הוא תיקו.

    Args:
        board: רשימה המייצגת את לוח המשחק.

    Returns:
        True אם תיקו, אחרת False.
    """
    # אם כל התאים מלאים ואין מנצח, אז המשחק מסתיים בתיקו
    return ' ' not in board[1:]


def play_tic_tac_toe():
    """
    הפונקציה הראשית להפעלת המשחק "איקס עיגול".
    """
    # מאתחלים את לוח המשחק
    board = [' '] * 10  # אינדקס 0 אינו בשימוש
    # לולאת המשחק הראשית
    while True:
        # מציגים את לוח המשחק
        print_board(board)

        # תור השחקן (אדם)
        while True:
            try:
                cellNumber = int(input("הכנס את מספר התא (1-9) עבור 'O': "))
                # בודקים את הקלט לתקינות
                if 1 <= cellNumber <= 9 and board[cellNumber] == ' ':
                    board[cellNumber] = 'O'
                    break
                else:
                    print("קלט שגוי. אנא הזן מספר תא פנוי בטווח 1-9.")
            except ValueError:
                print("קלט שגוי. אנא הזן מספר שלם בטווח 1-9.")
        # בודקים האם השחקן ניצח
        if check_win(board, 'O'):
            print_board(board)
            print("ניצחת! ברכות!")
            break

        # בודקים האם יש תיקו
        if check_draw(board):
            print_board(board)
            print("תיקו!")
            break

        # תור המחשב
        # מחפשים את התא הפנוי הראשון
        for i in range(1, 10):
            if board[i] == ' ':
                board[i] = 'X'
                break

        # בודקים האם המחשב ניצח
        if check_win(board, 'X'):
            print_board(board)
            print("המחשב ניצח!")
            break
        # בודקים האם יש תיקו
        if check_draw(board):
            print_board(board)
            print("תיקו!")
            break


# הפעלת המשחק
if __name__ == "__main__":
    play_tic_tac_toe()


