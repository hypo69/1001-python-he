# Function to print the game board
def print_board(board):
    """
    מציגה את המצב הנוכחי של לוח המשחק.

    Args:
        board: רשימה המייצגת את לוח המשחק.
    """
    print("\n   " + board[1] + " | " + board[2] + " | " + board[3])
    print("  ---+---+---")
    print("   " + board[4] + " | " + board[5] + " | " + board[6])
    print("  ---+---+---")
    print("   " + board[7] + " | " + board[8] + " | " + board[9] + "\n")


# Function to check for a win
def check_win(board, player):
    """
    בודקת האם קיימת קומבינציית ניצחון עבור השחקן הנתון.

    Args:
        board: רשימה המייצגת את לוח המשחק.
        player: סמל השחקן ('X' או 'O').

    Returns:
        True, אם השחקן ניצח, אחרת False.
    """
    # כל קומבינציות הניצחון האפשריות
    winning_combinations = [
        [1, 2, 3], [4, 5, 6], [7, 8, 9],  # Horizontal
        [1, 4, 7], [2, 5, 8], [3, 6, 9],  # Vertical
        [1, 5, 9], [3, 5, 7]  # Diagonal
    ]
    # עוברים על כל קומבינציה ובודקים האם היא תואמת לסמלי השחקן
    for combination in winning_combinations:
        if (board[combination[0]] == board[combination[1]] ==
                board[combination[2]] == player):
            return True
    return False


# Function to check for a draw
def check_draw(board):
    """
    בודקת האם המשחק הנוכחי הוא תיקו.

    Args:
        board: רשימה המייצגת את לוח המשחק.

    Returns:
        True, אם תיקו, אחרת False.
    """
    # אם כל המשבצות מלאות ואין מנצח, המשחק מסתיים בתיקו
    return ' ' not in board[1:]


def play_tic_tac_toe():
    """
    פונקציה ראשית להפעלת המשחק "איקס עיגול".
    """
    # מאתחלים את לוח המשחק
    board = [' '] * 10  # 0 אינדקס לא בשימוש
    # לולאת המשחק הראשית
    while True:
        # מציגים את לוח המשחק
        print_board(board)

        # מהלך השחקן (אדם)
        while True:
            try:
                cellNumber = int(input("הזן מספר משבצת (1-9) עבור 'O': "))
                # בודקים את תקינות הקלט
                if 1 <= cellNumber <= 9 and board[cellNumber] == ' ':
                    board[cellNumber] = 'O'
                    break
                else:
                    print("קלט לא תקין. אנא הזן מספר של משבצת פנויה בין 1 ל-9.")
            except ValueError:
                print("קלט לא תקין. אנא הזן מספר שלם בין 1 ל-9.")
        # בודקים האם השחקן ניצח
        if check_win(board, 'O'):
            print_board(board)
            print("ניצחת! מזל טוב!")
            break

        # בודקים האם המשחק הוא תיקו
        if check_draw(board):
            print_board(board)
            print("תיקו!")
            break

        # מהלך המחשב
        # מחפשים את המשבצת הפנויה הראשונה
        for i in range(1, 10):
            if board[i] == ' ':
                board[i] = 'X'
                break

        # בודקים האם המחשב ניצח
        if check_win(board, 'X'):
            print_board(board)
            print("המחשב ניצח!")
            break
        # בודקים האם המשחק הוא תיקו
        if check_draw(board):
            print_board(board)
            print("תיקו!")
            break


# הפעלת המשחק
if __name__ == "__main__":
    play_tic_tac_toe()


