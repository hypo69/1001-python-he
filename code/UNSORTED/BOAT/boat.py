import random

# אתחול לוח המשחק 8x8
board = [[0 for _ in range(8)] for _ in range(8)]

# פונקציה למיקום הספינה על הלוח
def place_ship():
    # בחירת כיוון אקראי (0 - אופקי, 1 - אנכי)
    direction = random.randint(0, 1)
    if direction == 0:  # כיוון אופקי
        row = random.randint(0, 7)
        col = random.randint(0, 4) # כדי שהספינה לא תחרוג מגבולות הלוח
        for i in range(3):
            board[row][col + i] = 1
    else: # כיוון אנכי
        row = random.randint(0, 4) # כדי שהספינה לא תחרוג מגבולות הלוח
        col = random.randint(0, 7)
        for i in range(3):
            board[row + i][col] = 1

# מיקום הספינה על הלוח
place_ship()

# לולאת המשחק הראשית
while True:
    # הצגת הלוח במסוף עבור המשתמש
    for row in board:
        print(" ".join(map(str, row)))

    # בקשת קלט קואורדינטות מהמשתמש
    try:
        coordinates = input("הזן קואורדינטות ירי (שורה, עמודה): ")
        row, col = map(int, coordinates.split(','))
        # בדיקת תקינות הקואורדינטות
        if not (0 <= row <= 7 and 0 <= col <= 7):
            print("קואורדינטות שגויות. הזן מספרים בין 0 ל-7")
            continue
    except ValueError:
        print("פורמט קלט שגוי. הזן שורה ועמודה מופרדים בפסיק, לדוגמה: 1,2")
        continue

    # בדיקה האם הירי היה פגיעה
    if board[row][col] == 1:
        print("פגיעה!")
        board[row][col] = 2  # החלפת הערך כדי לא להתחשב בפגיעות חוזרות
        # בדיקה האם נותרו חלקים נוספים של הספינה
        ship_sunk = True
        for row_b in board:
            if 1 in row_b:
                ship_sunk = False
                break
        if ship_sunk:
            print("הספינה הוטבעה! ניצחת!")
            break

    elif board[row][col] == 0:
        print("החטאה!")

    elif board[row][col] == 2:
         print("כבר ירית בתא זה. נסה תא אחר.")

