import random

# אתחול שדה המשחק 8x8
board = [[0 for _ in range(8)] for _ in range(8)]

# פונקציה למיקום ספינה על הלוח
def place_ship():
    # בוחרים כיוון אקראי (0 - אופקי, 1 - אנכי)
    direction = random.randint(0, 1)
    if direction == 0:  # כיוון אופקי
        row = random.randint(0, 7)
        col = random.randint(0, 4) # כדי שהספינה לא תצא מגבולות
        for i in range(3):
            board[row][col + i] = 1
    else: # כיוון אנכי
        row = random.randint(0, 4) # כדי שהספינה לא תצא מגבולות
        col = random.randint(0, 7)
        for i in range(3):
            board[row + i][col] = 1

# ממקמים את הספינה על השדה
place_ship()

# לולאת המשחק הראשית
while True:
    # מציגים את השדה במסוף למשתמש
    for row in board:
        print(" ".join(map(str, row)))

    # מבקשים קלט קואורדינטות מהמשתמש
    try:
        coordinates = input("הזן קואורדינטות ירייה (שורה, עמודה): ")
        row, col = map(int, coordinates.split(','))
        # בודקים תקינות קואורדינטות
        if not (0 <= row <= 7 and 0 <= col <= 7):
            print("קואורדינטות לא תקינות. הזן מספרים בין 0 ל-7")
            continue
    except ValueError:
        print("פורמט קלט לא תקין. הזן שורה ועמודה מופרדים בפסיק, לדוגמה: 1,2")
        continue

    # בודקים האם הירייה הייתה פגיעה
    if board[row][col] == 1:
        print("פגיעה!")
        board[row][col] = 2  # משנים את הערך כדי לא להתחשב בפגיעות חוזרות
        # בודקים אם נותרו עוד חלקי ספינה
        ship_sunk = True
        for row_b in board:
            if 1 in row_b:
                ship_sunk = False
                break
        if ship_sunk:
            print("הספינה הוטבעה! ניצחת!")
            break

    elif board[row][col] == 0:
        print("החטאה!")

    elif board[row][col] == 2:
         print("כבר ירית בתא זה. נסה תא אחר.")

