import numpy as np

# Функция для поворота куба вокруг оси X
def rotate_x(cube):
    """
    מסובבת את הקובייה ב-90 מעלות סביב ציר X.

    Args:
        cube (numpy.ndarray): מערך תלת-ממדי המייצג את הקובייה.

    Returns:
        numpy.ndarray: הקובייה לאחר הסיבוב.
    """
    return np.rot90(cube, k=-1, axes=(1, 2))

# Функция для поворота куба вокруг оси Y
def rotate_y(cube):
    """
        מסובבת את הקובייה ב-90 מעלות סביב ציר Y.

        Args:
            cube (numpy.ndarray): מערך תלת-ממדי המייצג את הקובייה.

        Returns:
            numpy.ndarray: הקובייה לאחר הסיבוב.
        """
    return np.rot90(cube, k=-1, axes=(0, 2))

# Функция для поворота куба вокруг оси Z
def rotate_z(cube):
    """
            מסובבת את הקובייה ב-90 מעלות סביב ציר Z.

            Args:
                cube (numpy.ndarray): מערך תלת-ממדי המייצג את הקובייה.

            Returns:
                numpy.ndarray: הקובייה לאחר הסיבוב.
            """
    return np.rot90(cube, k=-1, axes=(0, 1))

# Функция для получения положения кубика
def get_cube_position(cube, x, y, z):
    """
        מקבלת את המיקום הנוכחי של קובייה בתוך הקובייה הגדולה.

        Args:
            cube (numpy.ndarray): מערך תלת-ממדי המייצג את הקובייה.
            x (int): קואורדינטה של הקובייה בציר X (בין 1 ל-3).
            y (int): קואורדינטה של הקובייה בציר Y (בין 1 ל-3).
            z (int): קואורדינטה של הקובייה בציר Z (בין 1 ל-3).

        Returns:
            tuple: קואורדינטות הקובייה באוריינטציה הנוכחית של הקובייה הגדולה.
        """
    return cube[z-1, y-1, x-1]

# Инициализация куба 3x3x3
cube = np.arange(1, 28).reshape(3, 3, 3) # Создаем куб 3x3x3

# Игровой цикл
while True:
    command = input("Введите координаты (X Y Z) или команду ('X', 'Y', 'Z') или 'END' для выхода: ").upper()
    
    if command == "END" or command == "0":
        print("Игра завершена.")
        break
    
    parts = command.split()
    
    if len(parts) == 3:
        try:
            x, y, z = map(int, parts)
            if 1 <= x <= 3 and 1 <= y <= 3 and 1 <= z <= 3:
                pos = get_cube_position(cube, x, y, z)
                print(f"Положение кубика ({x}, {y}, {z}): {pos}")
            else:
                  print("Координаты должны быть целыми числами от 1 до 3.")
        except ValueError:
                print("Неверный формат координат.")
    elif len(parts) == 1:
        if command == "X":
            cube = rotate_x(cube)
            print("Куб повернут вокруг оси X.")
        elif command == "Y":
            cube = rotate_y(cube)
            print("Куб повернут вокруг оси Y.")
        elif command == "Z":
            cube = rotate_z(cube)
            print("Куб повернут вокруг оси Z.")
        else:
              print("Неизвестная команда.")
    else:
         print("Неверный формат ввода.")
         
