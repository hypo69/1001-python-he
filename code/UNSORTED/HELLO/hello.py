# משחק הלו
# תוכנית זו מציגה ברכה למשתמש.
# זוהי אחת התוכניות הפשוטות ביותר, המדגימה פקודות בסיסיות בפייתון.

# הצגת הברכה על המסך
print("שלום! ברוכים הבאים למשחק הלו!")  # שימוש בפונקציה print לצורך הצגת טקסט

# בקשת שם המשתמש
name = input("מה שמך? ")  # שימוש בפונקציה input לקבלת קלט מהמשתמש

# הצגת ברכה מותאמת אישית
print(f"שלום, {name}! נעים להכיר!")  # שימוש ב-f-string לשילוב השם בטקסט

# הודעה נוספת
print("תודה על השימוש בתוכנית הלו!")