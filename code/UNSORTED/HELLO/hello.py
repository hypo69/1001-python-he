# משחק Hello
# תוכנית זו מציגה ברכה למשתמש.
# זוהי אחת התוכניות הפשוטות ביותר, המדגימה פקודות Python בסיסיות.

# הצגת הברכה על המסך
print("שלום! ברוך הבא למשחק Hello!")  # השתמש בפונקציה print להצגת טקסט

# בקשת שם המשתמש
name = input("איך קוראים לך? ")  # השתמש בפונקציה input לקבלת נתונים מהמשתמש

# הצגת ברכה מותאמת אישית
print(f"שלום, {name}! נעים להכיר!")  # השתמש ב-f-string להטמעת השם בטקסט

# הודעה נוספת
print("תודה על השימוש בתוכנית Hello!")