    # מבקשים את מספר התאים החיים ההתחלתיים
    initial_alive_cells = int(input("הזן מספר תאים חיים התחלתיים: "))

    # מאתחלים ומציגים את המצב ההתחלתי של השדה
    grid = initialize_grid(num_rows, num_cols)
    place_alive_cells(grid, initial_alive_cells)
    print("מצב השדה ההתחלתי:")
    display_grid(grid)

    # לולאת המשחק הראשית
    while True:
        # מחשבים את הדור הבא
        next_grid = compute_next_generation(grid)
        grid = next_grid
