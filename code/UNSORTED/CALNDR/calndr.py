# מבקשים מהמשתמש את מספר היום בשבוע שממנו מתחיל החודש (1 - ראשון, 7 - שבת)
while True:
    try:
      startDay = int(input("הזן את היום בשבוע שממנו מתחיל החודש (1-ראשון, 7-שבת): "))
      if 1 <= startDay <= 7:
         break
      else:
        print("אנא הזן מספר בין 1 ל-7")
    except ValueError:
        print("אנא הזן מספר שלם.")

# מבקשים מהמשתמש את מספר הימים בחודש (28-31)
while True:
    try:
        daysInMonth = int(input("הזן את מספר הימים בחודש (28-31): "))
        if 28 <= daysInMonth <= 31:
            break
        else:
            print("אנא הזן מספר בין 28 ל-31")
    except ValueError:
          print("אנא הזן מספר שלם.")

# פולטים את הכותרת עם ימי השבוע
print(" SUN MON TUE WED THU FRI SAT")

# מחשבים את כמות הרווחים לפני המספר הראשון בחודש
spaces = (startDay - 1) * 4

# פולטים את הרווחים
print(" " * spaces, end="")

# פולטים את לוח השנה
for day in range(1, daysInMonth + 1):
    # פולטים את היום הנוכחי בחודש
    print(f"{day:3}", end=" ")

    # בודקים אם היום הנוכחי הוא שבת, עוברים לשורה חדשה
    if (startDay + day - 2) % 7 == 6:
        print()
    elif day != daysInMonth: # אם זה לא היום האחרון, מוסיפים רווח
      print(" ",end = "")
"""
הסבר קוד:
1.  **קלט נתונים**:
    -   `while True:` - לולאה אינסופית המאפשרת לחזור על קלט נתונים עד להזנת ערכים תקינים.
    -   `try...except ValueError`: בלוק try-except מטפל בשגיאות קלט אפשריות. אם המשתמש יזין ערך שאינו מספר שלם, תוצג הודעת שגיאה.
    -   `startDay = int(input(...))`: מבקש מהמשתמש את מספר היום בשבוע שממנו מתחיל החודש, וממיר אותו למספר שלם.
    -   `daysInMonth = int(input(...))`: מבקש מהמשתמש את מספר הימים בחודש, וממיר אותו למספר שלם.
    - `if 1 <= startDay <= 7:`: בדיקת תקינות של הערך שהוזן עבור `startDay` (1-ראשון, 7-שבת).
    - `if 28 <= daysInMonth <= 31`: בדיקת תקינות של הערך שהוזן עבור `daysInMonth` (מספר הימים בחודש).
2.  **פלט כותרת**:
    -   `print(" SUN MON TUE WED THU FRI SAT")`: פולט את השורה עם שמות ימי השבוע.
3.  **חישוב מרווחים**:
    -   `spaces = (startDay - 1) * 4`: מחשב את מספר הרווחים שיש לפלוט לפני היום הראשון בחודש. כל יום בשבוע תופס 4 תווים (3 ספרות + רווח).
4.  **פלט רווחים**:
    -   `print(" " * spaces, end="")`: פולט את כמות הרווחים הנדרשת לפני תחילת לוח השנה, `end=""` מבטל את המעבר האוטומטי לשורה חדשה אחרי `print`.
5.  **לולאת פלט לוח השנה**:
    -   `for day in range(1, daysInMonth + 1):`: הלולאה עוברת על כל ימי החודש.
    -   `print(f"{day:3}", end=" ")`: פולט את היום הנוכחי עם עיצוב (3 תווים) ורווח אחריו.
    -   `if (startDay + day - 2) % 7 == 6:`: בדיקה האם היום הנוכחי הוא שבת. אם שארית החלוקה ב-7 היא 6, זה יום שבת.
    -   `print()`: אם היום הנוכחי הוא שבת, עוברים לשורה חדשה.
    -    `elif day != daysInMonth:` אם היום אינו האחרון בחודש מוסיפים רווח.
"""