# הגדרת רשימת השאלות והתשובות
questions = [
    ("What is the capital of France?", "Paris"),
    ("What is 2 + 2?", "4"),
    ("What is the largest planet in our solar system?", "Jupiter"),
    ("What is the chemical symbol for water?", "H2O"),
    ("Who painted the Mona Lisa?", "Leonardo da Vinci")
]

# אתחול אינדקס השאלה הנוכחית
questionIndex = 0

# לולאת המשחק הראשית
while questionIndex < len(questions):
    # מציגים את השאלה הנוכחית
    currentQuestion, correctAnswer = questions[questionIndex]
    print(currentQuestion)

    # מבקשים קלט תשובה מהמשתמש
    userAnswer = input("Your answer: ")

    # בודקים את נכונות התשובה
    if userAnswer.lower() == correctAnswer.lower():
        print("RIGHT")
    else:
        print("WRONG")

    # מגדילים את אינדקס השאלה
    questionIndex += 1

# מציגים הודעה על סיום המשחק
print("THAT'S ALL FOLKS!")

"""
הסבר קוד:
1. **אתחול שאלות ותשובות**:
   - `questions = [...]`: נוצרת רשימה `questions`, המכילה טאפלים (tuples). כל טאפל מייצג זוג: שאלה (מחרוזת) ותשובה נכונה (מחרוזת).
   - `questionIndex = 0`: מאותחלת משתנה `questionIndex` למעקב אחר השאלה הנוכחית, ממנה מתחיל החידון.
2. **לולאה ראשית `while questionIndex < len(questions):`**:
   - הלולאה ממשיכה כל עוד `questionIndex` קטן מאורך הרשימה `questions`, כלומר, עד שכל השאלות יישאלו.
   - `currentQuestion, correctAnswer = questions[questionIndex]`: מחלץ את השאלה הנוכחית והתשובה הנכונה מרשימת השאלות לפי האינדקס הנוכחי.
   - `print(currentQuestion)`: מציג את השאלה הנוכחית על המסך.
   - **קלט תשובה**:
     - `userAnswer = input("Your answer: ")`: מבקש קלט תשובה מהמשתמש ושומר אותו במשתנה `userAnswer`.
   - **בדיקת תשובה**:
     - `if userAnswer.lower() == correctAnswer.lower():`: משווה את תשובת המשתמש (לאחר המרה לאותיות קטנות) עם התשובה הנכונה (גם היא לאחר המרה לאותיות קטנות) להשוואה שאינה תלויה ברישיות (case-insensitive).
     - `print("RIGHT")`: מציג את ההודעה "RIGHT" אם התשובה נכונה.
     - `else:`: אם התשובה שגויה.
     - `print("WRONG")`: מציג את ההודעה "WRONG" אם התשובה שגויה.
   - `questionIndex += 1`: מגדיל את `questionIndex` ב-1 כדי לעבור לשאלה הבאה.
3. **הצגת הודעה על סיום המשחק**:
   - `print("THAT'S ALL FOLKS!")`: מציג את ההודעה על סיום המשחק לאחר שהלולאה מסתיימת (כלומר, כל השאלות נשאלו).

"""