import random

# אתחול גובה הטיל וכמות הדלק
rocketHeight = 0
fuel = 100

# לולאת המשחק הראשית
while rocketHeight < 100 and fuel > 0:
    print(f"גובה: {rocketHeight}, דלק: {fuel}")
    try:
        fuelToBurn = int(input("כמה דלק לשרוף?: "))
    except ValueError:
        print("אנא הזן מספר שלם.")
        continue
    if fuelToBurn > fuel:
        print("אין לך מספיק דלק!")
    else:
        fuel -= fuelToBurn
        rise = random.randint(0, 2 * fuelToBurn)
        rocketHeight += rise

# בדיקת תנאי סיום המשחק והצגת הודעה
if rocketHeight >= 100:
    print("ברכות! הגעת לגובה 100!")
else:
    print("ניצלת את כל הדלק. לא הגעת לגובה 100!")

