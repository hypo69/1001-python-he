import random

# 1. אתחול:
# 1.1. יצירת לוח ריק בגודל 15x15
board = [[' ' for _ in range(15)] for _ in range(15)]
# 1.2. הגדרת דגל תחילת המשחק G = 1
game_on = True
# 1.3. הגדרת דגל השחקן, P = 1 (שחקן 1 הולך ראשון)
current_player = 1

# פונקציה להצגת הלוח בקונסולה
def print_board(board):
    print("   ", end="")
    for i in range(15):
         print(f"{i:2}", end=" ")
    print()
    print("  " + "----" * 15)
    for i, row in enumerate(board):
        print(f"{i:2}|", end=" ")
        for cell in row:
            print(f"{cell:2}", end=" ")
        print()
    print()


# פונקציה לבדיקת נוכחות קו ניצחון
def check_winner(board, player):
    symbol = 'X' if player == 1 else 'O'
    # בדיקת קווים אופקיים
    for row in board:
        for i in range(len(row) - 4):
            if row[i] == row[i + 1] == row[i + 2] == row[i + 3] == row[i + 4] == symbol:
                return True
    # בדיקת קווים אנכיים
    for col in range(15):
        for i in range(15 - 4):
            if board[i][col] == board[i + 1][col] == board[i + 2][col] == board[i + 3][col] == board[i + 4][col] == symbol:
                 return True
    # בדיקת אלכסונים (מלמעלה-משמאל למטה-ימין)
    for row in range(15 - 4):
        for col in range(15 - 4):
            if board[row][col] == board[row+1][col+1] == board[row+2][col+2] == board[row+3][col+3] == board[row+4][col+4] == symbol:
                 return True
    # בדיקת אלכסונים (מלמעלה-מימין למטה-שמאל)
    for row in range(15 - 4):
        for col in range(4, 15):
            if board[row][col] == board[row+1][col-1] == board[row+2][col-2] == board[row+3][col-3] == board[row+4][col-4] == symbol:
                 return True
    return False


# פונקציה לבדיקת תיקו
def check_full_board(board):
    for row in board:
        for cell in row:
            if cell == ' ':
                return False # אם יש לפחות תא פנוי אחד, אז המשחק לא הסתיים
    return True  # אם אין תאים ריקים - תיקו

# 2. לולאת המשחק הראשית
while game_on:
    # 2.1. הצגת מצב הלוח הנוכחי
    print_board(board)

    # 2.2. תור השחקן (P = 1)
    if current_player == 1:
        while True:
             try:
                # 2.2.1. בקשת קואורדינטות מהלך X, Y
                x = int(input("Игрок 1 (X) введите x (0-14): "))
                y = int(input("Игрок 1 (X) введите y (0-14): "))

                # 2.2.2. בדיקת תקינות המהלך:
                if 0 <= x < 15 and 0 <= y < 15 and board[x][y] == ' ':
                    # 2.2.3. הצבת סמל המהלך של השחקן הנוכחי (X) על הלוח
                    board[x][y] = 'X'
                    break
                else:
                    print("Недопустимый ход. Попробуйте еще раз.")
             except ValueError:
                  print("Некорректный ввод. Пожалуйста, введите целые числа")
        # 2.2.4. שינוי דגל השחקן
        current_player = 2
    # 2.3. תור המחשב (P = 2)
    else:
        print("Ход компьютера:")
        while True:
            # 2.3.1. המחשב בוחר מהלך אקראי
            x = random.randint(0, 14)
            y = random.randint(0, 14)
            if board[x][y] == ' ':
                 # 2.3.2. הצבת סמל המהלך של השחקן הנוכחי (O) על הלוח
                board[x][y] = 'O'
                break

        # 2.3.3. שינוי דגל השחקן
        current_player = 1

    # 2.4. בדיקה למנצח או לתיקו
    if check_winner(board, 1 if current_player == 2 else 2 ):
        print_board(board)
        print(f"Победил игрок {'1 (X)' if current_player == 2 else '2 (O)'}!")
        game_on = False  # סיום המשחק
    elif check_full_board(board):
         print_board(board)
         print("Ничья!")
         game_on = False  # סיום המשחק

