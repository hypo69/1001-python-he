"""
FIPFOP:
=================
דרגת קושי: 5
-----------------
המשחק FIPFOP הוא משחק חידה שבו השחקן נדרש, בתורו, להזין מספרים.
מטרת המשחק היא להביא לכך שסכום כל המספרים שהוזנו על ידי המשתמש יגיע ל-100.
המשחק מסתיים כאשר סכום כל המספרים שהוזנו מגיע ל-100.

כללי המשחק:
1. השחקן מזין מספרים שלמים, אחד בכל פעם.
2. כל מספר שהוזן נוסף לסכום הכולל.
3. המשחק מסתיים כאשר הסכום הכולל מגיע ל-100.
4. לאחר סיום המשחק, מוצג מספר המספרים הכולל שהוזנו (מהלכים).
-----------------
אלגוריתם:
1. אתחל את הסכום ל-0.
2. אתחל את מונה המהלכים ל-0.
3. התחל לולאה "כל עוד הסכום קטן מ-100":
    3.1 בקש מהשחקן להזין מספר.
    3.2 הגדל את מונה המהלכים ב-1.
    3.3 הוסף את המספר שהוזן לסכום הכולל.
4. הצג הודעה "השתמשת ב-{מספר מהלכים} מהלכים".
5. סוף המשחק.
-----------------

"""


# Initialize sum and turn counter
totalSum = 0 # Sum of entered numbers
numberOfTurns = 0 # Number of turns

# Main game loop
while totalSum < 100:
    # Prompt user for number input
    try:
        userNumber = int(input("Введите число: "))
    except ValueError:
         print("Пожалуйста, введите целое число.")
         continue

    # Increment turn counter
    numberOfTurns += 1
    # Add entered number to total sum
    totalSum += userNumber

# Print message about number of turns
print(f"ТЫ ИСПОЛЬЗОВАЛ {numberOfTurns} ХОДОВ")


"""
הסבר קוד:
1. **אתחול משתנים:**
   - `totalSum = 0`: מאתחל את המשתנה `totalSum` לאחסון סכום המספרים שהוזנו; מתחילים מ-0.
   - `numberOfTurns = 0`: מאתחל את המשתנה `numberOfTurns` לספירת מספר המהלכים; גם כן מתחילים מ-0.
2. **הלולאה הראשית `while totalSum < 100`:**
   - הלולאה ממשיכה כל עוד סכום המספרים שהוזנו (`totalSum`) קטן מ-100.
3. **קבלת קלט מהמשתמש:**
    - `try...except ValueError`: בלוק try-except מטפל בשגיאות קלט אפשריות. אם המשתמש מזין ערך שאינו מספר שלם, תוצג הודעת שגיאה.
   - `userNumber = int(input("Введите число: "))`: מבקש מהמשתמש להזין מספר וממיר אותו למספר שלם, ושומר את התוצאה במשתנה `userNumber`.
4. **הגדלת מונה המהלכים:**
   - `numberOfTurns += 1`: מגדיל את מונה המהלכים ב-1 בכל איטרציה של הלולאה.
5.  **הוספת המספר לסכום:**
   -  `totalSum += userNumber`: מוסיף את המספר שהוזן (`userNumber`) לסכום הכולל (`totalSum`).
6.  **הצגת התוצאה:**
    - `print(f"ТЫ ИСПОЛЬЗОВАЛ {numberOfTurns} ХОДОВ")`: מציג על המסך הודעה על מספר המהלכים שבוצעו כאשר סכום המספרים הגיע ל-100 או יותר.
"""