"""
ANIMAL:
=================
רמת קושי: 4
-----------------
המשחק "ANIMAL" הוא משחק ניחוש בעלי חיים, שבו המחשב בוחר באופן אקראי בעל חיים מתוך רשימה, והשחקן מנסה לנחש אותו על ידי הזנת השערותיו. המשחק נמשך עד שהשחקן מנחש נכונה את בעל החיים.

כללי המשחק:
1.  המחשב בוחר בעל חיים אקראי מרשימה מוגדרת מראש.
2.  השחקן מזין את השערותיו לגבי בעל החיים הנעלם.
3.  לאחר כל ניסיון, המחשב מדווח האם השחקן ניחש נכונה את בעל החיים או לא.
4.  המשחק נמשך עד שהשחקן מנחש את בעל החיים הנעלם.
-----------------
אלגוריתם:
1.  הגדרת רשימת בעלי החיים.
2.  בחירת בעל חיים אקראי מהרשימה.
3.  התחלת לולאה "כל עוד בעל החיים לא נוחש":
    3.1 בקשת הזנת שם בעל החיים מהשחקן.
    3.2 אם השם שהוזן זהה לבעל החיים הנעלם, עבור לשלב 4.
    3.3 אחרת, הצג את ההודעה "לא".
4. הצגת ההודעה "צדקת!".
5. סיום המשחק.
-----------------
"""
import random

# רשימת בעלי החיים למשחק
animals = ['DOG', 'CAT', 'HORSE', 'COW', 'PIG']

# בחירת בעל חיים אקראי מהרשימה
targetAnimal = random.choice(animals)

# התחלת לולאה, כל עוד בעל החיים לא נוחש
while True:
    # בקשת הזנת שם בעל החיים מהמשתמש
    userAnimal = input("נחשו את בעל החיים (DOG, CAT, HORSE, COW, PIG): ").upper()

    # בדיקה האם המשתמש ניחש את בעל החיים
    if userAnimal == targetAnimal:
        print("צדקת!") # דיווח על תשובה נכונה
        break # סיום הלולאה אם בעל החיים נוחש
    else:
        print("לא") # דיווח על תשובה לא נכונה