import math


# קבוע עבור תאוצת הכבידה החופשית (בפיט לשנייה בריבוע)
GRAVITY = 32.2

# התחלת לולאת המשחק
while True:
    try:
        # בקשת מהירות התחלתית מהמשתמש
        velocity = float(input("Введите начальную скорость ракеты (футы/сек) (отрицательное значение для выхода): "))

        # בדיקה אם המשתמש רוצה לצאת מהמשחק
        if velocity < 0:
            print("Игра завершена.")
            break # אם המהירות שלילית, יוצאים מהלולאה

        # בקשת זווית שיפוע מהמשתמש
        angle = float(input("Введите угол наклона (в градусах): "))

        # המרת זווית ממעלות לרדיאנים
        angle_radians = math.radians(angle)

        # חישוב טווח הטיסה של הטיל לפי הנוסחה
        distance = (velocity**2 * math.sin(2 * angle_radians)) / GRAVITY

        # הצגת הטווח המחושב
        print(f"Дальность полета ракеты: {distance:.2f} футов")

        # בדיקה אם הטיל הגיע ליעד
        if distance >= 1000:
            print("GOOD SHOT")
        else:
            print("SHORT SHOT")
    except ValueError:
        print("Ошибка ввода. Пожалуйста, введите числовое значение.")

