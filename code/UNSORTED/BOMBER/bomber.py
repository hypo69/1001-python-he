"""
import random
import math

# מייצרים מרחק אקראי למטרה מ-100 עד 1000
targetDistance = random.randint(100, 1000)

# מבקשים קלט עבור זווית הטלה ומרחק הטלה של הפצצה
try:
    angle = float(input("הזן את זווית הטלת הפצצה במעלות: "))
    distance = float(input("הזן את מרחק הטלת הפצצה: "))
except ValueError:
    print("שגיאה: אנא הזן ערכים מספריים עבור הזווית והמרחק.")
    exit()

# ממירים את הזווית ממעלות לרדיאנים
angleInRadians = angle * math.pi / 180

# מחשבים את המרחק אליו תיפול הפצצה
dropDistance = distance * math.cos(angleInRadians)

# מחשבים את ההפרש בין מרחק נפילת הפצצה למרחק המטרה
distanceDifference = abs(dropDistance - targetDistance)

# בודקים אם הפצצה פגעה במטרה
if distanceDifference <= 10:
    print("ברכות! פגעת במטרה!")
else:
    print("פספסת!")
"""
