"""
BASKET:
=================
מורכבות: 4
-----------------
המשחק "כדורסל" הוא משחק טקסטואלי המדמה זריקות כדורסל. השחקן בוחר את מרחק הזריקה (מ-10 עד 60 רגל), והמחשב קובע את תוצאת הזריקה (פגיעה או החטאה) על בסיס הסתברות התלויה במרחק. המשחק נמשך עד שהשחקן מחליט לסיים אותו.

כללי המשחק:
1. השחקן מזין את מרחק הזריקה ברגל (מספר שלם מ-10 עד 60).
2. המחשב מחשב את הסתברות הפגיעה על בסיס המרחק.
3. המחשב מייצר מספר אקראי ומשווה אותו להסתברות הפגיעה.
4. אם המספר האקראי קטן או שווה להסתברות, הזריקה נחשבת מוצלחת (פגיעה), אחרת - בלתי מוצלחת (החטאה).
5. המשחק מציג את תוצאת הזריקה (פגיעה או החטאה).
6. השחקן יכול להמשיך בזריקות עד שיזין 0 לסיום המשחק.
-----------------
אלגוריתם:
1. התחל לולאה "עד שהשחקן יזין 0":
    1.1 בקש מהשחקן את מרחק הזריקה ברגל (10-60, או 0 לסיום).
    1.2 אם המרחק שווה ל-0, סיים את הלולאה.
    1.3 אם המרחק קטן מ-10 או גדול מ-60, הצג הודעת שגיאה ועבור לתחילת הלולאה.
    1.4 חשב את הסתברות הפגיעה לפי הנוסחה: הסתברות = (60 - מרחק) / 50.
    1.5 ייצר מספר אקראי מ-0 עד 1.
    1.6 אם המספר האקראי קטן או שווה להסתברות, הצג הודעה "That's a hit!".
    1.7 אחרת, הצג הודעה "You missed it!".
2. סוף המשחק.
-----------------
תרשים זרימה:
```mermaid
flowchart TD
    Start["התחלה"] --> LoopStart{"התחלת לולאה: עד שהמרחק לא שווה 0"}
    LoopStart --> InputDistance["הזנת מרחק: <code><b>distance</b></code>"]
    InputDistance --> CheckEnd{"בדיקה: <code><b>distance == 0?</b></code>"}
    CheckEnd -- Да --> End["סוף"]
    CheckEnd -- Нет --> CheckRange{"בדיקה: <code><b>10 <= distance <= 60?</b></code>"}
    CheckRange -- Нет --> OutputError["הצגת הודעת שגיאה: <b>Distance must be between 10 and 60</b>"]
    OutputError --> LoopStart
    CheckRange -- Да --> CalculateProbability["<code><b>probability = (60 - distance) / 50</b></code>"]
    CalculateProbability --> GenerateRandom["יצירת מספר אקראי: <code><b>random_number</b></code>"]
    GenerateRandom --> CheckHit{"בדיקה: <code><b>random_number <= probability?</b></code>"}
    CheckHit -- Да --> OutputHit["הצגת הודעה: <b>That's a hit!</b>"]
    OutputHit --> LoopStart
    CheckHit -- Нет --> OutputMiss["הצגת הודעה: <b>You missed it!</b>"]
    OutputMiss --> LoopStart
```

מקרא:
    Start - התחלת התוכנית.
    LoopStart - התחלת לולאה, שנמשכת עד שהשחקן מזין 0.
    InputDistance - בקשה מהמשתמש להזין את מרחק הזריקה ושמירתו במשתנה distance.
    CheckEnd - בדיקה האם המרחק שהוזן שווה ל-0.
    End - סוף התוכנית.
    CheckRange - בדיקה האם המרחק שהוזן נמצא בטווח שבין 10 ל-60.
    OutputError - הצגת הודעת שגיאה אם המרחק מחוץ לטווח המותר.
    CalculateProbability - חישוב הסתברות הפגיעה על בסיס המרחק שהוזן.
    GenerateRandom - יצירת מספר אקראי מ-0 עד 1.
    CheckHit - בדיקה האם המספר האקראי קטן או שווה להסתברות המחושבת.
    OutputHit - הצגת הודעה על פגיעה "That's a hit!".
    OutputMiss - הצגת הודעה על החטאה "You missed it!".
"""
import random

# לולאת המשחק הראשית
while True:
    # בקשה מהמשתמש להזין את מרחק הזריקה
    try:
        distance = int(input("הזן את מרחק הזריקה (10-60 רגל, 0 ליציאה): "))
    except ValueError:
        print("נא הזן מספר שלם.")
        continue

    # בדיקה האם השחקן רוצה לסיים את המשחק
    if distance == 0:
        print("המשחק הסתיים.")
        break

    # בדיקה האם המרחק נמצא בטווח המותר
    if distance < 10 or distance > 60:
        print("המרחק צריך להיות בין 10 ל-60 רגל.")
        continue

    # חישוב הסתברות הפגיעה
    probability = (60 - distance) / 50

    # יצירת מספר אקראי בין 0 ל-1
    random_number = random.random()

    # בדיקה האם הזריקה הייתה מוצלחת
    if random_number <= probability:
        print("That's a hit!")  # הצגת הודעה על פגיעה
    else:
        print("You missed it!")  # הצגת הודעה על החטאה
"""
הסבר הקוד:
1.  **ייבוא מודול `random`**:
   -  `import random`: מייבא את מודול `random`, המשמש ליצירת מספרים אקראיים.
2.  **הלולאה הראשית `while True:`**:
    -  לולאה אינסופית שנמשכת עד שהשחקן מזין 0.
    -   **קלט נתונים**:
        -  `try...except ValueError`: בלוק try-except המטפל בשגיאות קלט אפשריות. אם המשתמש יזין ערך שאינו מספר שלם, תוצג הודעת שגיאה.
        -  ``distance = int(input("הזן את מרחק הזריקה (10-60 רגל, 0 ליציאה): "))`: מבקש מהמשתמש את מרחק הזריקה וממיר אותו למספר שלם, שומר את התוצאה במשתנה `distance`.
    -   **תנאי יציאה**:
        -   `if distance == 0:`: בודק האם המרחק שווה ל-0.
        -  `print("המשחק הסתיים.")`: מציג הודעה על סיום המשחק.
        -  `break`: מסיים את הלולאה.
    -  **בדיקת מרחק**:
        -   `if distance < 10 or distance > 60:`: בודק האם המרחק נמצא בטווח המותר שבין 10 ל-60.
        -  `print("המרחק צריך להיות בין 10 ל-60 רגל.")`: מציג הודעת שגיאה אם המרחק מחוץ לטווח.
        -  `continue`: ממשיך לאיטרציה הבאה של הלולאה.
    -   **חישוב הסתברות**:
        -  `probability = (60 - distance) / 50`: מחשב את הסתברות הפגיעה על בסיס הנוסחה מהמשחק המקורי.
    -   **יצירת מספר אקראי**:
        - `random_number = random.random()`: מייצר מספר אקראי מ-0 עד 1 ושומר אותו ב-`random_number`.
    -   **בדיקת תוצאה**:
        -   `if random_number <= probability:`: בודק האם המספר האקראי קטן או שווה להסתברות המחושבת.
        -   `print("That's a hit!")`: מציג הודעה על פגיעה.
        -   `else`: אם המספר האקראי גדול מההסתברות.
        -   `print("You missed it!")`: מציג הודעה על החטאה.
"""