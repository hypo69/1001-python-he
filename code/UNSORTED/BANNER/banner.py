# משחק באנר: יצירת באנר טקסטואלי

# פונקציה ליצירת באנר
def create_banner(text):
    """
    יוצר באנר טקסטואלי מהטקסט שהוזן.
    :param text: מחרוזת שיש להמיר לבאנר.
    :return: None
    """
    # מגדירים את רוחב הבאנר (אורך הטקסט + 4 תווים למסגרת)
    banner_width = len(text) + 4

    # מדפיסים את הגבול העליון של הבאנר
    print("*" * banner_width)

    # מדפיסים את הטקסט עם המסגרת
    print(f"* {text} *")

    # מדפיסים את הגבול התחתון של הבאנר
    print("*" * banner_width)

# החלק הראשי של התוכנית
if __name__ == "__main__":
    # ברכת המשתמש
    print("ברוכים הבאים למשחק Banner!")
    print("הזינו טקסט, ואני אצור עבורכם באנר טקסטואלי.")

    # מבקשים מהמשתמש טקסט
    user_text = input("הזינו טקסט: ")

    # בודקים שהטקסט אינו ריק
    if user_text.strip() == "":
        print("לא הזנתם טקסט. אנא נסו שוב.")
    else:
        # יוצרים ומדפיסים את הבאנר
        print("\nהבאנר שלכם מוכן:")
        create_banner(user_text)